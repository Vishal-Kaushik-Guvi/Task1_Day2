// Ques 1 :- Write a program to print the numbers from 10-50 using for/while loop.

package Task1_Day2;

public class Solution1{
    public static void main(String[] args) {
    int lastNumber=50;
        // Represent the last number..
       for(int i=10; i<=lastNumber; i++){
            System.out.println(i);
       }
    }
}